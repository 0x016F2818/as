[SECTION .data]

disp_pos			dd	0

[SECTION .text]


global	__puts
global	enable_int
global	disable_int
; ========================================================================
;                  void __puts(char * pszInfo);
; ========================================================================
__puts:
	push	ebp
	mov	ebp, esp

	mov	esi, [ebp + 8]	; pszInfo
	mov	edi, [disp_pos]
	mov	ah, 0Fh
.1:
	lodsb
	test	al, al
	jz	.2
	cmp	al, 0Ah	; is '\n'
	jnz	.3
	push	eax
	mov	eax, edi
	mov	bl, 160
	div	bl
	and	eax, 0FFh
	inc	eax
	mov	bl, 160
	mul	bl
	mov	edi, eax
	pop	eax
	jmp	.1
.3:
	mov	[gs:edi], ax
	add	edi, 2
	jmp	.1

.2:
	mov	[disp_pos], edi

	pop	ebp
	ret

;;  ========================================================================
;;                   void disable_int();
;;  ========================================================================
disable_int:
		cli
		ret

;;  ========================================================================
;;                   void enable_int();
;;  ========================================================================
enable_int:
		sti
		ret
